    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $productCategory = request('productCategory');
        
        if($productCategory == null || $productCategory == ''){
            return redirect()->back()->withInput()->withErrors(['Error' => 'Please enter a valid Product Category!']);
        }

        $alreadyExists = DB::table('product_categories')
        ->where('name', '=', $productCategory)
        ->get();

        if(count($alreadyExists)>0){
            return redirect()->back()->withInput()->withErrors(['Error' => 'Product Category already exists.']);
        }

        $newProductCategory = new productCategory;
        $newProductCategory->name = $productCategory;
        $newProductCategory->save();

        //dd("saved successfully");
        
        return redirect()->route('product.index');
    }

